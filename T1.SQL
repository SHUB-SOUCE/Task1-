CREATE DATABASE HOSPITAL_MGMT_SYSTEM ;
USE HOSPITAL_MGMT_SYSTEM;
-- HERE, IN THIS DATABASE OF HOSPITAL_MGMT_SYSTEM :
-- - PATIENT CONSULTS (RELATION) DOCTOR AND THEN DOCTOR GUIDES NURSE TO WORK 
-- - PATIENT ASSIGNED (RELATION) ROOMS
-- - PATIENT HAS (RELATION) TEST_REPORTS
-- - PATIENT PAYS (RELATION) BILLS
-- - NURSE IS A(RELATION) EMPLOYEE
-- - NURSE GOVERNS(RELATION) ROOMS

CREATE TABLE PATIENT(P_ID VARCHAR(10) PRIMARY KEY ,DOB date , MOB_NO int, AGE int,  P_NAME Varchar(30),GENDER varchar(1));
CREATE TABLE DOCTOR(DEPT VARCHAR(30), QUALIFICATION VARCHAR(20));
CREATE TABLE EMPLOYEE(E_ID VARCHAR(10) PRIMARY KEY, E_NAME Varchar(30), SALARY INT, MOB_NO int,STATE varchar(20), CITY VARCHAR(20), PIN_NO VARCHAR(10));
CREATE TABLE NURSE(EID VARCHAR(10), FOREIGN KEY (EID) REFERENCES EMPLOYEE (E_ID));
CREATE TABLE ROOMS(R_ID VARCHAR(10) PRIMARY KEY,TYPE VARCHAR(10), CAPACITY INT3, AVAILABILITY INT2);
CREATE TABLE TEST_REPORT (PID VARCHAR(10),FOREIGN KEY (PID) REFERENCES patient (P_ID), RID VARCHAR(10),FOREIGN KEY (RID) REFERENCES rooms (R_ID),RESULT VARCHAR(10), TEST_TYPE varchar(10));
CREATE TABLE BILLS(B_ID varchar(10) primary key, BPID VARCHAR(10) ,FOREIGN KEY (BPID) REFERENCES patient (P_ID), AMOUNT FLOAT(6,2));

DESCRIBE PATIENT;
DESCRIBE DOCTOR;
DESCRIBE NURSE
DESCRIBE EMPLOYEE;
DESCRIBE ROOMS;
DESCRIBE TEST_REPORT;
DESCRIBE BILLS;

